#include "ft_select.h"

int	get_size(t_beg *b)
{
	struct winsize	w;
	int				i;

	if ((i = ioctl(0, TIOCGWINSZ, &w) > -1))
		;
	b->x = w.ws_col;
	b->y = w.ws_row;
	return (i);
}
